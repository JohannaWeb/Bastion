pub fn draw_char(
    buffer: &mut [u32],
    w: u32,
    h: u32,
    x: u32,
    y: u32,
    ch: char,
    color: u32,
) {
    draw_char_styled(buffer, w, h, x, y, ch, color, false, false);
}

pub fn draw_char_styled(
    buffer: &mut [u32],
    w: u32,
    h: u32,
    x: u32,
    y: u32,
    ch: char,
    color: u32,
    bold: bool,
    italic: bool,
) {
    let glyph = get_glyph(ch);
    for (row, &bits) in glyph.iter().enumerate() {
        let py = y + row as u32;
        if py >= h {
            break;
        }

        let italic_offset = if italic { (7 - row as i32) / 3 } else { 0 };

        for col in 0..8 {
            if (bits & (1 << (7 - col))) != 0 {
                let px = (x as i32 + col as i32 + italic_offset) as u32;
                if px < w {
                    let idx = (py * w + px) as usize;
                    if idx < buffer.len() {
                        buffer[idx] = color;

                        if bold && (px + 1) < w {
                            let bold_idx = (py * w + (px + 1)) as usize;
                            if bold_idx < buffer.len() {
                                buffer[bold_idx] = color;
                            }
                        }
                    }
                }
            }
        }
    }
}

